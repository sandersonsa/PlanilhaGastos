    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String resultado = verificarPreenchimento();
        if(resultado.equals("OK")){
            try{
                Receita r = new Receita();
                r.setOrigem(txtFontePagadora.getText().trim());
                r.setReferencia(Utils.formatarReferencia(cmbReferencia.getSelectedItem().toString()));
                r.setValor(Utils.getTxtToValorBigDecimal(txtValor));
                new DaoReceita().salvarReceita(r);
                dispose();
            }catch(Exception e){
                throw new RuntimeException("Erro ao confirmar novo registro", e);
            }
        } else {
            JOptionPane.showMessageDialog(null, resultado, "Alerta", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(JOptionPane.showConfirmDialog(null, "Deseja realmente cancelar?", "Aviso", JOptionPane.YES_NO_OPTION)
                == JOptionPane.YES_OPTION) {
            dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private String verificarPreenchimento(){
        StringBuilder resultado = new StringBuilder();
        if(cmbReferencia.getSelectedItem() == null){
            resultado.append("Selecione o tipo de Registro");
        }else if(txtFontePagadora.getText().trim().isEmpty()){
            resultado.append("Informe a fonte pagadora");
        }else if(txtValor.getText().trim().equals(Utils.VALOR_VAZIO)
                || txtValor.getText().trim().equals(Utils.VAZIO)){
            resultado.append("Informe um valor para a receita");
        }else {
            resultado.append("OK");
        }
        return resultado.toString();
    }

